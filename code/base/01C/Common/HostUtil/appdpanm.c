//-----------------------------------------------------------------------------
//! \brief
//!     Appends Primary Account Number (PAN) manually entered to send data
//!
//! \return
//!     None.
//-----------------------------------------------------------------------------
extern void AppendPanMan( void )
{
	UBYTE RemBytes;

	// Append the Primary Account Number (PAN) manually entered to the 
	// send data
	RemBytes = MovPanAsc( ( char * ) pSendBuffer, TRREQ.TRPAN, MAXPANL );

	// Increment the send data pointer
	pSendBuffer += ( MAXPANL - RemBytes );
}

