//-----------------------------------------------------------------------------
//!  \brief     Convert Byte From Binary to BCD
//!
//!  \param
//!     ubBIN       binary value to convert
//!
//!  \return
//!     UBYTE       BCD number converted from binary
//!
//-----------------------------------------------------------------------------
extern UBYTE BinBcdB( UBYTE ubBIN )
{
	UBYTE retval;

	retval = ubBIN / 10;
	retval <<= 4;
	retval |= ( ubBIN % 10 );

	return retval;
}



