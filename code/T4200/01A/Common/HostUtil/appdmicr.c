//-----------------------------------------------------------------------------
//! \brief
//!     Appends Customer's Check Reader Information to send data
//!
//! \return
//!     None.
//-----------------------------------------------------------------------------
extern void AppendMICR( void )
{
	// Append the Customer's Check Reader Information to the send data
	pSendBuffer += MoveTillZero(
			( char * ) pSendBuffer,
			( char * ) TRREQ.micr.TRMICR,
			sizeof( TRREQ.micr.TRMICR ) );
}


