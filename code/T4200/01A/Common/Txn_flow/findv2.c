//-----------------------------------------------------------------------------
//!  \brief
//!     Return Ptr to Visa 2 Table for The acq ID given
//!
//!  \param
//!     acq_id      acq id to match.
//!
//!  \return
//!     v2_rec       struct Visa2 pointer or NULL if does not exist.
//!
//-----------------------------------------------------------------------------
extern struct v2_rec *FindVisa2( UBYTE acq_id )
{
	UBYTE i;

	for ( i = 0; i < V2MAX; i++ )
	{
		if ( V2TAB[i].V2AQID == acq_id )
		{
			return ( struct v2_rec * ) &V2TAB[i];
		}
	}
	return ( ( struct v2_rec * ) NULL );
}

