//-----------------------------------------------------------------------------
//!
//!      Display an error message and halts the application
//!
//! \param
//!
//! \note
//!		This function NEVER returns!!!
//!
extern void SystemErrMsg( void )
{
	SDK_GuiClearScr(  );
	SDK_GuiText( 0, 0, Dspbuf );
	SDK_GuiDispAll(  );

	// Loop forever
	while ( True )
	{
		SDK_RelinqCPU(  );
	}
}


