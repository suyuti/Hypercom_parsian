//-----------------------------------------------------------------------------
//  AppendPanTrack2     Appends Primary Account Number (PAN) from Track 2 
//                      data to send data
//
//  Parameters:         None.
//
//  Global Inputs:      None.
//
//  Returns:            Nothing.
//
//  Notes:
//
//-----------------------------------------------------------------------------

extern void AppendPanTrack2( void )
{
	// Append the Primary Account Number (PAN) from Track 2 data to send data
	pSendBuffer += MoveTillChar(
			( char * ) pSendBuffer,
			( char * ) TRREQ.TRTRK2, MAXTRK2,
			( char ) 0xff );
}

