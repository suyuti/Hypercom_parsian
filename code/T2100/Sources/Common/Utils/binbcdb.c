//-----------------------------------------------------------------------------
//  BinBcdB         Convert Byte From Binary to BCD
//
//  Parameters:     ubBIN       binary value to convert
//
//  Global Inputs:
//
//  Returns:        UBYTE       BCD number converted from binary
//
//  Notes:
//
//-----------------------------------------------------------------------------

extern UBYTE BinBcdB( UBYTE ubBIN )
{
	UBYTE retval;

	retval = ubBIN / 10;
	retval <<= 4;
	retval |= ( ubBIN % 10 );

	return retval;
}



