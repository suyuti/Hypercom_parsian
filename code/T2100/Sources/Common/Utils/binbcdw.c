//-----------------------------------------------------------------------------
//  BinBcdW         Convert Word From Binary to BCD
//
//  Parameters:     UWORD uwBIN     binary value to convert
//
//  Global Inputs:
//
//  Returns:        UWORD   BCD number converted from binary
//
//  Notes:
//
//-----------------------------------------------------------------------------

extern UWORD BinBcdW( UWORD uwBIN )
{
	UBYTE retval[2];
	UBYTE tmp;
	UWORD *retword;

	// Calculate 100's part of binary word
	retval[0] = uwBIN / 100;

	// Calculate 10's part of word 
	retval[1] = uwBIN - ( retval[0] * 100 );
	tmp = ( ( retval[1] / 10 ) * 16 );
	retval[1] = ( retval[1] % 10 ) + tmp;

	retword = ( UWORD * ) retval;

	return ( *retword );
}

