//-----------------------------------------------------------------------------
//  SetTCTabLRC     Set the Checksum (LRC) for the Terminal Configuration Table
//
//  Parameters:     None.
//
//  Global Inputs:  TCONF.
//
//  Returns:        Nothing.
//
//  Notes:          Modifies global TCLRC.
//
//-----------------------------------------------------------------------------

extern void SetTCTabLRC( void )
{
	TCLRC = CalcLrc( ( UBYTE * ) & TCONF, TCSZE );
}


