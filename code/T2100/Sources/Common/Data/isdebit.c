//-----------------------------------------------------------------------------
//IsDebitTrans      Checks if Transaction is a Debit Type (DEBIT,
//                  DBCASH, REFDEBIT, or SALCASH)
//
//  Parameters:     enum transactions Tran      Transaction to check.
//
//  Global Inputs:
//
//  Returns:        Bool        True  - Transaction is a Debit Type
//                              False - Transaction is not a Debit Type.
//
//  Notes:
//
//-----------------------------------------------------------------------------

extern Bool IsDebitTrans( enum transactions Tran )
{
	// See if Transaction is a Debit Type
	if ( ( DEBIT == Tran ) || ( DBCASH == Tran ) || ( REFDEBIT == Tran ) ||
		 ( SALCASH == Tran ) )
	{
		// Yes; return true
		return ( True );
	}
	else
	{
		// No; return false
		return ( False );
	}
}

