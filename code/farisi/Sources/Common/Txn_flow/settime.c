//-----------------------------------------------------------------------------
//!	\brief
//!     Set Transaction Date/Time.
//!
//!	\param
//!     pDateTime       Buffer to store current Date/Time.
//!
//!	\return
//!     None
//!
//-----------------------------------------------------------------------------
extern void SetTranTime( UBYTE * pDateTime )
{
	UBYTE RtcBuf[RTCLEN];

	/* Get Current Date/Time */
	SDK_RtcRead( RtcBuf );

	/* Set Transaction Date/Time to Current Date/Time */
	AscHex( pDateTime, ( char * ) RtcBuf, S_TRYEAR + S_TRDATE + S_TRTIME );
}


